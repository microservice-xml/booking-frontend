import { toast } from "react-toastify";

export function SuccesMessage(message: string) {
  return toast.success(message, {
    position: "top-center",
    autoClose: 1000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    style: { fontSize: "1.6rem" },
  });
}

